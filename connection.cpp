#include <iostream>
/*place holder*/
using namespace std;

int main() {
  int a = 5;
  int b = 3;
  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  float quotient = (float)a / b;

  cout << "Sum: " << sum << endl;
  cout << "Difference: " << difference << endl;
  cout << "Product: " << product << endl;
  cout << "Quotient: " << quotient << endl;

  return 0;
}